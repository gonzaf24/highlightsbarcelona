    <div class="article">
        <h1 class="title">Metro (Subway)</h1>
        <div class="container">
            <p class="subtitle">
                Barcelona's metro system is an efficient and widely used mode of transportation, connecting various neighborhoods and major attractions.
            </p>
            <h2 class="subtitle">
                <strong>Ticket:</strong> Tickets can be purchased at ticket machines in metro stations. In the machines you can pay with cash or credit card. 
                There are different types of tickets available, including single tickets, T-10 cards (10 trips), and unlimited travel cards (HOLA Barcelona Card).
                <br>
                <strong>Price:</strong> A single ticket costs around €2.40, and the T-10 card is approximately €11.35.
                <br>
                <strong>Schedule:</strong> The metro operates from approximately 5:00 AM to midnight on weekdays. 
                    On Fridays, the metro extends its hours until 2:00 AM, which means it stays open until Saturday at 2:00 AM.
                    Additionally, on Saturdays, the metro runs all day long, which means it remains open even after midnight.
                    For example, the metro will still be operational on Sunday at 4:00 AM.        
                <br>
                <strong>Trick:</strong> Machines accept coins and bills.
                The ticket metro also works for the bus, tram, and train. You can use the same ticket for all of these modes of transportation.
                <br>
                <strong>Official Website:</strong> https://www.tmb.cat/en/home
            </h2>  
        </div>
    </div>

