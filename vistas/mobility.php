        <h1 class="title">Airport Shuttle (Aerobus)</h1>
        <div class="container">
            <p class="subtitle">
                The Aerobus is a dedicated airport shuttle service connecting Barcelona-El Prat Airport with the city center.
                <span class="t1">T1</span> and <span class="t2">T2</span> are the two terminals at Barcelona-El Prat Airport.
            </p>
            <h2 class="subtitle">
                <strong>Ticket:</strong> Tickets can be purchased at the airport, onboard the bus or online HERE.
                <br>
                <strong>Price:</strong> A one-way ticket costs approximately 6,75 €, and a round-trip ticket is around 11,65 €.
                <br>
                <strong>Service:</strong> Aerobus operates 24 hours a day, 365 days a year. It offers two lines that run continuously to both Terminal 1 (T1) and Terminal 2 (T2) of Barcelona Airport.
                <br>
                <strong>Schedule:</strong> Aerobus services run frequently, with departures every 5-10 minutes during peak hours.
                <br>
                <strong>Route:</strong> Airport to Plaça Catalunya (city center), travel time: 35 minutes
                <br>
                <strong>Trick:</strong> The Aerobus is a convenient option for travelers who want to get to the city center chaep, quickly and comfortably.
                    Taxi cost approximately 30 € to Plaça Catalunya (city center).
                    In Terminal 2 (T2), there is a train that takes you directly to the city center for 2.20 €. However, if you are in Terminal 1 (T1), you need to take a free bus to Terminal 2 
                    (internal airport shuttle) and then take the train to the Barcelona city center. 
                <br>
                <strong>Official Website:</strong> https://www.aerobusbcn.com
            </h2>
