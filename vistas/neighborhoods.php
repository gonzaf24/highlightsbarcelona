        <h1 class="title">Eixample</h1>
        <div class="container">
            <p class="subtitle">
                A spacious and grid-like neighborhood known for its modernist architecture,
                including the iconic Sagrada Família and Casa Batlló.
                Eixample offers upscale shopping and dining experiences.
            </p>
            <div class="columns">
                <div class="column">
                    <img src="./img/eixample.jpg" alt="Eixample" class="img">
                </div>
                <div class="column">
                    <p class="subtitle">
                    The Eixample (Catalan pronunciation: [əˈʃampɫə], Catalan for 'expansion' or 'Expansion 
                    District') is a district of Barcelona between the old city (Ciutat Vella) and what were 
                    once surrounding small towns (Sants, Gràcia, Sant Andreu etc.), constructed in the 19th 
                    and early 20th centuries. Its population was 262,485 at the last census (2005).
                    </p>
