        <h1 class="title">Barri Gòtic (Gothic Quarter)(City center)</h1>
        <div class="container">
            <p class="subtitle">
                The historic heart of Barcelona, characterized by narrow medieval streets,
                charming plazas, and historic buildings. It is home to the Barcelona Cathedral and many shops,
                cafes, and restaurants.
            </p>
            <div class="columns">
                <div class="column">
                    <p class="subtitle">
                    The Gothic Quarter (Catalan: Barri Gòtic IPA: [ˈbari ˈɣɔtik] or El Gòtic, Spanish: Barrio 
                    Gótico) is the centre of the old city of Barcelona. It stretches from La Rambla to Via 
                    Laietana, and from the Mediterranean seafront to the Ronda de Sant Pere. It is a part of 
                    Ciutat Vella district.
                    </p>
