        <h1 class="title">Poble Nou</h1>
        <div class="container">
            <p class="subtitle">
            A beachside and former industrial area transformed into a modern and dynamic neighborhood 
            with a mix of tech companies, local businesses, and green spaces.
            Poble Nou is known for its contemporary architecture and proximity to the beach.
            </p>
            <div class="columns">
                <div class="column">
                    <img src="./img/poblenou.jpg" alt="Poble Nou" class="img">
                </div>
                <div class="column">
                    <p class="subtitle">
                    Poble Nou is a neighborhood in the Sant Martí district of Barcelona, Catalonia, Spain. 
                    Before 1714, the area on which the neighborhood would eventually be built was known as 
                    Poblenou del Mar, a large area of land between the city of Barcelona and the then-separate 
                    town of Gràcia. The area was essentially industrial, with many textile factories located 
                    along the Rec Comtal, as well as a large military fort (the citadel) overlooking the 
                    neighborhood. However, the area was far from heavily populated, there being only a few 
                    buildings here and there, such as the Torre de les Aigües ("Water Tower"), built in 1771 
                    and since demolished.
                    </p>
