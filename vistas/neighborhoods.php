        <h1 class="title">El Born (El Borne)(City center)</h1>
        <div class="container">
            <p class="subtitle">
                A trendy neighborhood known for its artistic vibe, boutique shops, and vibrant nightlife.
                El Born is home to the Picasso Museum and offers a mix of old and modern architecture.
            </p>
            <div class="columns">
                <div class="column">
                    <img src="./img/elborn.jpg" alt="El Born" class="img">
                </div>
                <div class="column">
                    <p class="subtitle">
                        El Born is one of the four neighborhoods that make up the Ciutat Vella ("Old City") of 
                        Barcelona. This neighborhood is full of narrow, winding streets and has long been home 
                        to artists and bohemians. It currently has many trendy bars and restaurants, but it also 
                        still retains its medieval charm. This neighborhood is comparable to the Village, 
                        Greenwich Village, or SoHo in New York City.
                    </p>
