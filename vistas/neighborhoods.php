        <h1 class="title">Gracia</h1>
        <div class="container">
            <p class="subtitle">
                A bohemian neighborhood with a vibrant nightlife, Gracia is known for its narrow streets, 
                independent boutiques, and charming squares. The area is also home to the annual Festa Major 
                de Gracia, a week-long festival with colorful street decorations, live music, and more.
            </p>
            <div class="columns">
                <div class="column">
                    <p class="subtitle">
                    Gràcia is a district of the city of Barcelona, Catalonia, Spain. It comprises the 
                    neighborhoods of Vila de Gràcia, Vallcarca i els Penitents, El Coll, La Salut and 
                    Camp d'en Grassot i Gràcia Nova. Gràcia is bordered by the districts of Eixample to 
                    the south, Sarrià-Sant Gervasi to the west and Horta-Guinardó to the east. A vibrant 
                    and diverse enclave of Catalan life, Gràcia was an independent municipality for centuries 
                    before being formally annexed by Barcelona in 1897 as a part of the city's expansion.
                    </p>
