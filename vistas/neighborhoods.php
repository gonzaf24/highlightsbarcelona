        <h1 class="title">Barceloneta</h1>
        <div class="container">
            <p class="subtitle">
                A beachside neighborhood with a maritime charm, Barceloneta is famous for 
                its sandy beaches, seafood restaurants, and waterfront promenade.
            </p>
            <div class="columns">
                <div class="column">
                    <p class="subtitle">
                    La Barceloneta (Catalan pronunciation: [lə βəɾsəluˈnɛtə]) is a neighborhood in the Ciutat Vella 
                    district of Barcelona, Catalonia, Spain. The neighborhood was constructed during the 18th 
                    century for the residents of the Ribera neighborhood who had been displaced by the 
                    construction of the Ciutadella of Barcelona. The neighborhood is roughly triangular, 
                    bordered by the Mediterranean Sea, the Moll d'Espanya of Port Vell, and the El Born 
                    neighborhood. This neighborhood even has its own flag. The neighborhood is serviced by 
                    its own stop on the Barcelona Metro (L4).
                    </p>
